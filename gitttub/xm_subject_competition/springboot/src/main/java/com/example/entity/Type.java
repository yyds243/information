package com.example.entity;


/**
 * 赛事类型
*/
public class Type {
    /** ID */
    private Integer id;
    /** 名称 */
    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}